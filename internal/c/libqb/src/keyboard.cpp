static int constexpr TranslateDirectKey(GLUTEmu_KeyboardKey key) {
    for (const auto &entry : kDirectKeyMap) {
        if (entry.key == key) {
            return entry.qbKey;
        }
    }

    return -1;
}

static int constexpr TranslateKeypadDigitOrDecimal(GLUTEmu_KeyboardKey key, bool useKeypadNumber) {
    if (key >= GLUTEmu_KeyboardKey::KP0 && key <= GLUTEmu_KeyboardKey::KP9) {
        const int offset = int(key) - int(GLUTEmu_KeyboardKey::KP0);
        return useKeypadNumber ? (VK + QBVK_KP0 + offset) : (QBK + offset);
    }

    if (key == GLUTEmu_KeyboardKey::KPDecimal) {
        return useKeypadNumber ? (VK + QBVK_KP_PERIOD) : (QBK + 10);
    }

    return -1;
}

static int constexpr TranslateLetter(GLUTEmu_KeyboardKey key, bool isShift, bool isControl, bool isAlt, bool isCapsLock) {
    if (key < GLUTEmu_KeyboardKey::A || key > GLUTEmu_KeyboardKey::Z) {
        return -1;
    }

    const int offset = int(key) - int(GLUTEmu_KeyboardKey::A);

    if (isControl && !isAlt) {
        int ctrlValue = offset + 1;
        return isShift ? (ctrlValue - 1 + 'A') : (ctrlValue - 1 + 'a');
    }

    return (isShift ^ isCapsLock ? 'A' : 'a') + offset;
}

static int constexpr TranslateDigit(GLUTEmu_KeyboardKey key, bool isShift, bool isControl, bool isAlt) {
    if (key < GLUTEmu_KeyboardKey::Zero || key > GLUTEmu_KeyboardKey::Nine) {
        return -1;
    }

    const int offset = int(key) - int(GLUTEmu_KeyboardKey::Zero);
    int translated = '0' + offset;

    if (isShift) {
        translated = kShiftedDigits[offset];
    }

    if (isControl && !isAlt && key == GLUTEmu_KeyboardKey::Six) {
        translated = 30;
    }

    return translated;
}

static int constexpr TranslatePunctuation(GLUTEmu_KeyboardKey key, bool isShift, bool isControl, bool isAlt) {
    for (const auto &entry : kPunctuationMap) {
        if (entry.key == key) {
            if (isControl && !isAlt && entry.control != -1) {
                return entry.control;
            }

            return isShift ? entry.shifted : entry.normal;
        }
    }

    return -1;
}

static int constexpr TranslatePrintableKey(GLUTEmu_KeyboardKey key, bool isShift, bool isControl, bool isAlt, bool isCapsLock) {
    int translated = TranslateLetter(key, isShift, isControl, isAlt, isCapsLock);
    if (translated != -1) {
        return translated;
    }

    translated = TranslateDigit(key, isShift, isControl, isAlt);
    if (translated != -1) {
        return translated;
    }

    if (key == GLUTEmu_KeyboardKey::Space) {
        return ' ';
    }

    return TranslatePunctuation(key, isShift, isControl, isAlt);
}

static int constexpr TranslateKey(GLUTEmu_KeyboardKey key, bool isShift, bool isControl, bool isAlt, bool isCapsLock, bool useKeypadNumber) {
    int translated = TranslateDirectKey(key);
    if (translated != -1) {
        return translated;
    }

    translated = TranslateKeypadDigitOrDecimal(key, useKeypadNumber);
    if (translated != -1) {
        return translated;
    }

    return TranslatePrintableKey(key, isShift, isControl, isAlt, isCapsLock);
}

int32_t keyboard_scancode_get_scancode(int32_t keyIndex) {
    return keyboard_scancode_lookup_table[keyIndex * kScancodeEntryWidth + 1];
}

int32_t keyboard_scancode_get_variant(int32_t keyIndex, int32_t modifierOffset) {
    return keyboard_scancode_lookup_table[keyIndex * kScancodeEntryWidth + 2 + modifierOffset];
}

bool keyboard_scancode_has_variant(int32_t keyIndex, int32_t modifierOffset) {
    return keyboard_scancode_get_variant(keyIndex, modifierOffset) != 0;
}

bool keyboard_keyup_mask_consume(uint32_t key) {
    for (int32_t i = 0; i <= keyboard_keyup_mask_last; i++) {
        if (key == keyboard_keyup_mask[i]) {
            keyboard_keyup_mask[i] = 0;
            return true;
        }
    }

    return false;
}

void keyboard_keyup_mask_add(uint32_t key) {
    int32_t i = 0;
    for (i = 0; i <= keyboard_keyup_mask_last; i++) {
        if (!keyboard_keyup_mask[i]) {
            keyboard_keyup_mask[i] = key;
            return;
        }
    }

    if (i == keyboard_keyup_mask_last + 1 && keyboard_keyup_mask_last < 255) {
        keyboard_keyup_mask[i] = key;
        keyboard_keyup_mask_last++;
    }
}

bool keyboard_try_translate_numpad_keyhit(uint32_t key, uint32_t *normalizedKey, int64_t *keyhitFlag) {
    *normalizedKey = key;
    *keyhitFlag = 0;

    if ((key >= (VK + QBVK_KP0)) && (key <= (VK + QBVK_KP_ENTER))) {
        *keyhitFlag = 4294967296ll;

        if ((key >= (VK + QBVK_KP0)) && (key <= (VK + QBVK_KP9))) {
            *normalizedKey = key - (VK + QBVK_KP0) + 48;
            return true;
        }

        switch (key) {
        case VK + QBVK_KP_PERIOD:
            *normalizedKey = 46;
            return true;
        case VK + QBVK_KP_DIVIDE:
            *normalizedKey = 47;
            return true;
        case VK + QBVK_KP_MULTIPLY:
            *normalizedKey = 42;
            return true;
        case VK + QBVK_KP_MINUS:
            *normalizedKey = 45;
            return true;
        case VK + QBVK_KP_PLUS:
            *normalizedKey = 43;
            return true;
        case VK + QBVK_KP_ENTER:
            *normalizedKey = 13;
            return true;
        }
    }

    if ((key >= (QBK + 0)) && (key <= (QBK + 10))) {
        *keyhitFlag = 4294967296ll;
        const uint32_t offset = key - QBK;
        static constexpr uint16_t kQbkNumpadLookup[] = {82 << 8, 79 << 8, 80 << 8, 81 << 8, 75 << 8, 76 << 8, 77 << 8, 71 << 8, 72 << 8, 73 << 8, 83 << 8};

        *normalizedKey = kQbkNumpadLookup[offset];
        return true;
    }

    return false;
}

bool keyboard_try_get_modifier_data(uint32_t key, uint8_t *scancode, int32_t *flagsMask) {
    switch (key) {
    case VK + QBVK_LSHIFT:
        *scancode = 42;
        *flagsMask = 3;
        return true;
    case VK + QBVK_RSHIFT:
        *scancode = 54;
        *flagsMask = 3;
        return true;
    case VK + QBVK_LALT:
    case VK + QBVK_RALT:
        *scancode = 56;
        *flagsMask = 8;
        return true;
    case VK + QBVK_LCTRL:
    case VK + QBVK_RCTRL:
        *scancode = 29;
        *flagsMask = 4;
        return true;
    case VK + QBVK_NUMLOCK:
        *scancode = 69;
        *flagsMask = 32;
        return true;
    case VK + QBVK_CAPSLOCK:
        *scancode = 58;
        *flagsMask = 64;
        return true;
    case VK + QBVK_SCROLLOCK:
        *scancode = 70;
        *flagsMask = 0;
        return true;
    default:
        return false;
    }
}

void keyboard_get_onkey_scancode_and_flags(uint32_t key, int32_t *scancode, int32_t *flagsMask) {
    if (key <= 255) {
        *scancode = keyboard_scancode_get_scancode(key);
    } else {
        uint32_t x3 = key;

        if ((x3 >= (VK + QBVK_KP0)) && (x3 <= (VK + QBVK_KP_ENTER))) {
            x3 = (x3 - (VK + QBVK_KP0) + 256) * 256;
        } else if ((x3 >= (QBK + 0)) && (x3 <= (QBK + 0 + (QBVK_KP_PERIOD - QBVK_KP0)))) {
            x3 = (x3 - (QBK + 0) + 256) * 256;
        }

        if (x3 <= 65535) {
            int32_t r = (x3 >> 8) + 256;
            if (keyboard_scancode_has_variant(r, 0))
                *scancode = keyboard_scancode_get_scancode(r);
        }
    }

    uint8_t modifierScancode;
    if (keyboard_try_get_modifier_data(key, &modifierScancode, flagsMask))
        *scancode = modifierScancode;
}

bool keyboard_is_onkey_extended_key(uint32_t key) {
    switch (key) {
    case 0x4B00:
    case 0x4800:
    case 0x4D00:
    case 0x5000:
    case VK + QBVK_KP_DIVIDE:
    case VK + QBVK_KP_ENTER:
    case 0x5200:
    case 0x4700:
    case 0x4900:
    case 0x5300:
    case 0x4F00:
    case 0x5100:
    case VK + QBVK_RCTRL:
    case VK + QBVK_RALT:
        return true;
    default:
        return false;
    }
}

void keyup(uint32_t x) {
    if (!x)
        x = QBK + QBK_CHR0;

    keyheld_remove(x);

    if (asciicode_reading != 2) { // hide numpad presses related to ALT+1+2+3 type entries
        // identify and revert numpad specific key codes to non-numpad codes
        static uint32_t x2;
        static int64_t numpadkey;
        keyboard_try_translate_numpad_keyhit(x, &x2, &numpadkey);

        if (keyboard_keyup_mask_consume(x))
            goto key_handled;

        // add x2 to keyhit buffer
        static int32_t z;
        z = (keyhit_nextfree + 1) & 0x1FFF;
        if (z == keyhit_next) { // remove oldest message when cyclic buffer is full
            keyhit_next = (keyhit_next + 1) & 0x1FFF;
        }
        static int32_t sx;
        sx = x2;
        sx = -sx;
        x2 = sx; // negate x2
        keyhit[keyhit_nextfree] = x2 | numpadkey;
        keyhit_nextfree = z;
    } // asciicode_reading!=2

    static int32_t numlock;
    numlock = 0;

    if (x <= 255) {
        if (keyboard_scancode_has_variant(x, 0))
            scancodeup(keyboard_scancode_get_scancode(x));
        goto key_handled;
    } // x<=255

    // NUMPAD?
    if ((x >= (VK + QBVK_KP0)) && (x <= (VK + QBVK_KP_ENTER))) {
        if ((x >= (VK + QBVK_KP0)) && (x <= (VK + QBVK_KP_PERIOD)))
            numlock = 1;
        x = (x - (VK + QBVK_KP0) + 256) * 256;
        goto numpadkey;
    }
    if ((x >= (QBK + 0)) && (x <= (QBK + 0 + (QBVK_KP_PERIOD - QBVK_KP0)))) {
        x = (x - (QBK + 0) + 256) * 256;
        goto numpadkey;
    }

    if (x <= 65535) {
        static int32_t r;
    numpadkey:
        r = (x >> 8) + 256;
        if (keyboard_scancode_has_variant(r, 0))
            scancodeup(keyboard_scancode_get_scancode(r));

        if (x == 0x5200) { // INSERT lock emulation
            update_shift_state();
        }

        goto key_handled;
    } // x<=65536

    { // scope
        uint8_t modifierScancode;
        int32_t modifierFlagsMask;
        if (keyboard_try_get_modifier_data(x, &modifierScancode, &modifierFlagsMask)) {
            (void)modifierFlagsMask;
            scancodeup(modifierScancode);
            update_shift_state();
        }
    }

key_handled:;
}

void update_shift_state() {
    int32_t x;
    /*
        0:417h                   Shift Status
        7 6 5 4 3 2 1 0
        x . . . . . . .      Insert locked
        . x . . . . . .      Caps Lock locked
        . . x . . . . .      Num Lock locked
        . . . x . . . .      Scroll Lock locked
        . . . . x . . .      Alt key is pressed
        . . . . . x . .      Ctrl key is pressed
        . . . . . . x .      Left Shift key is pressed
        . . . . . . . x      Right Shift key is pressed
    */
    x = 0;
    if (keyheld(VK + QBVK_RSHIFT))
        x |= 1;
    if (keyheld(VK + QBVK_LSHIFT))
        x |= 2;
    if (keyheld(VK + QBVK_LCTRL) || keyheld(VK + QBVK_RCTRL))
        x |= 4;
    if (keyheld(VK + QBVK_LALT) || keyheld(VK + QBVK_RALT))
        x |= 8;
    if (keyheld(QBK + QBK_SCROLL_LOCK_MODE))
        x |= 16;
    if (keyheld(VK + QBVK_NUMLOCK))
        x |= 32;
    if (keyheld(VK + QBVK_CAPSLOCK))
        x |= 64;
    // note: insert state is emulated (off by default)
    if (keyheld(QBK + QBK_INSERT_MODE))
        x |= 128;
    cmem[0x417] = x;
    /*
        0:418h                   Extended Shift Status
        (interpret the word 'pressed' as "being held down")
        7 6 5 4 3 2 1 0
        x . . . . . . .      Ins key is pressed
        . x . . . . . .      Caps Lock key is pressed (detection not possible, return 0)
        . . x . . . . .      Num Lock key is pressed (detection not possible, return 0)
        . . . x . . . .      Scroll Lock key is pressed
        . . . . x . . .      Pause key locked
        . . . . . x . .      SysReq key is pressed
        . . . . . . x .      Left Alt key is pressed
        . . . . . . . x      Left Ctrl key is pressed
    */
    x = 0;
    if (keyheld(VK + QBVK_LCTRL))
        x |= 1;
    if (keyheld(VK + QBVK_LALT))
        x |= 2;
    if (keyheld(VK + QBVK_SYSREQ))
        x |= 4;
    if (keyheld(VK + QBVK_PAUSE))
        x |= 8;
    if (keyheld(VK + QBVK_SCROLLOCK))
        x |= 16;
    // if (keyheld(VK+QBVK_NUMLOCK)) x|=32;
    // if (keyheld(VK+QBVK_CAPSLOCK)) x|=64;
    if (keyheld(0x5200))
        x |= 128;
    cmem[0x418] = x;
    /*
        0:496h                   Keyboard Status and Type Flags
        This byte holds keyboard status information.
        Keyboard Status Information
        7 6 5 4 3 2 1 0
        x . . . . . . .       Read ID in progress (always 0)
        . x . . . . . .       Last character was first ID character (always 0)
        . . x . . . . .       Force Num Lock if read ID and KBX (always 0)
        . . . x . . . .       101/102-key keyboard installed (always 1)
        . . . . x . . .       Right Alt key is pressed
        . . . . . x . .       Right Ctrl key is pressed
        . . . . . . x .       Last code was E0 Hidden Code (always 0)
        . . . . . . . x       last code was E1 Hidden Code (always 0)
    */
    x = 0;
    if (keyheld(VK + QBVK_RCTRL))
        x |= 1;
    if (keyheld(VK + QBVK_RALT))
        x |= 2;
    x |= 16;
    cmem[0x496] = x;
}

void GLUT_KEYBOARD_BUTTON_FUNC(GLUTEmu_KeyboardKey key, int scancode, GLUTEmu_ButtonAction action, int modifiers) {
    (void)scancode;

    const bool isNumLock = modifiers & GLUTEmu_KeyboardKeyModifier::NumLock;
    const bool isShift = modifiers & GLUTEmu_KeyboardKeyModifier::Shift;
    const bool isControl = modifiers & GLUTEmu_KeyboardKeyModifier::Control;
    const bool isAlt = modifiers & GLUTEmu_KeyboardKeyModifier::Alt;
    const bool isCapsLock = modifiers & GLUTEmu_KeyboardKeyModifier::CapsLock;
    const bool useKeypadNumber = isAlt || (isNumLock && !isShift);

    int qbKey = TranslateKey(key, isShift, isControl, isAlt, isCapsLock, useKeypadNumber);

    if (qbKey != -1) {
        switch (action) {
        case GLUTEmu_ButtonAction::Pressed:
        case GLUTEmu_ButtonAction::Repeated:
            keydown(qbKey);
            break;

        case GLUTEmu_ButtonAction::Released:
            keyup(qbKey);
            break;
        }
    }
}
