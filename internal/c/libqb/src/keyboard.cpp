static int32_t keyheld(uint32_t x) {
    for (int32_t i = 0; i < keyheld_n; i++) {
        if (keyheld_buffer[i] == x)
            return 1;
    }

    // check multimapped NUMPAD keys
    if ((x >= 42) && (x <= 57)) {
        if ((x >= 48) && (x <= 57))
            return keyheld(VK + QBVK_KP0 + (x - 48)); // 0-9
        if (x == 46)
            return keyheld(VK + QBVK_KP_PERIOD);
        if (x == 47)
            return keyheld(VK + QBVK_KP_DIVIDE);
        if (x == 42)
            return keyheld(VK + QBVK_KP_MULTIPLY);
        if (x == 45)
            return keyheld(VK + QBVK_KP_MINUS);
        if (x == 43)
            return keyheld(VK + QBVK_KP_PLUS);
    }
    if (x == 13)
        return keyheld(VK + QBVK_KP_ENTER);
    if (x & kAsciiMask) {
        const uint32_t x2 = (x >> 8) & 255;
        if ((x2 >= 71) && (x2 <= 83)) {
            if (x2 == 82)
                return keyheld(QBK + QBVK_KP0 - QBVK_KP0);
            if (x2 == 79)
                return keyheld(QBK + QBVK_KP1 - QBVK_KP0);
            if (x2 == 80)
                return keyheld(QBK + QBVK_KP2 - QBVK_KP0);
            if (x2 == 81)
                return keyheld(QBK + QBVK_KP3 - QBVK_KP0);
            if (x2 == 75)
                return keyheld(QBK + QBVK_KP4 - QBVK_KP0);
            if (x2 == 76)
                return keyheld(QBK + QBVK_KP5 - QBVK_KP0);
            if (x2 == 77)
                return keyheld(QBK + QBVK_KP6 - QBVK_KP0);
            if (x2 == 71)
                return keyheld(QBK + QBVK_KP7 - QBVK_KP0);
            if (x2 == 72)
                return keyheld(QBK + QBVK_KP8 - QBVK_KP0);
            if (x2 == 73)
                return keyheld(QBK + QBVK_KP9 - QBVK_KP0);
            if (x2 == 83)
                return keyheld(QBK + QBVK_KP_PERIOD - QBVK_KP0);
        }
    }

    return 0;
}

static inline void keyheld_add(uint32_t x) {
    for (int32_t i = 0; i < keyheld_n; i++) {
        if (keyheld_buffer[i] == x)
            return;
    } // already in buffer

    if (keyheld_n == keyheld_size) {
        keyheld_size++;
        keyheld_buffer = (uint32_t *)realloc(keyheld_buffer, keyheld_size * 4);
        keyheld_bind_buffer = (uint32_t *)realloc(keyheld_bind_buffer, keyheld_size * 4);
    } // expand buffer

    keyheld_buffer[keyheld_n] = x; // add entry
    keyheld_bind_buffer[keyheld_n] = bindkey;
    bindkey = 0; // add binded key (0=none)
    keyheld_n++; // note: inc. must occur after setting entry (threading reasons)
}

static inline void keyheld_remove(uint32_t x) {
    for (int32_t i = 0; i < keyheld_n; i++) {
        if (keyheld_buffer[i] == x) { // exists
            memmove(&keyheld_buffer[i], &keyheld_buffer[i + 1], (keyheld_n - i - 1) * 4);
            memmove(&keyheld_bind_buffer[i], &keyheld_bind_buffer[i + 1], (keyheld_n - i - 1) * 4);
            keyheld_n--; // note: dec. must occur after memmove (threading reasons)
            return;
        }
    }
}

static inline void keyheld_unbind(uint32_t x) {
    for (int32_t i = 0; i < keyheld_n; i++) {
        if (keyheld_bind_buffer[i] == x) { // exists
            keyup(keyheld_buffer[i]);
            return;
        }
    }
}

static inline void scancodedown(uint8_t scancode) {
    if (port60h_events) {
        if (port60h_event[port60h_events - 1] == scancode)
            return; // avoid duplicate entries in buffer (eg. from key-repeats)
    }
    if (port60h_events == kPort60hEventCapacity) {
        memmove(port60h_event, port60h_event + 1, kPort60hEventMaxIndex);
        port60h_events = kPort60hEventMaxIndex;
    }
    port60h_event[port60h_events] = scancode;
    port60h_events++;
}

static inline void scancodeup(uint8_t scancode) {
    if (port60h_events) {
        if (port60h_event[port60h_events - 1] == (scancode + kScancodeReleaseBias))
            return; // avoid duplicate entries in buffer
    }
    if (port60h_events == kPort60hEventCapacity) {
        memmove(port60h_event, port60h_event + 1, kPort60hEventMaxIndex);
        port60h_events = kPort60hEventMaxIndex;
    }
    port60h_event[port60h_events] = scancode + kScancodeReleaseBias;
    port60h_events++;
}

static constexpr inline uint32_t unicode_to_cp437(uint32_t x) {
    for (int32_t i = 0; i <= 255; i++) {
        if (x == codepage437_to_unicode16[i])
            return i;
    }

    return 0;
}

static inline void keydown_unicode(uint32_t x) {
    keydown_glyph = 1;

    // note: UNICODE 0-127 map directly to ASCII 0-127
    if (x <= 127) {
        keydown(x);
        return;
    }

    // note: some UNICODE values map directly to CP437 values found in the extended ASCII set
    auto x2 = unicode_to_cp437(x);
    if (x2) {
        keydown(x2);
        return;
    }

    // note: full width latin characters will be mapped to their normal width equivalents
    // Wikipedia note: Range U+FF01\96FF5E reproduces the characters of ASCII 21 to 7E as fullwidth forms, that is, a fixed width form used in CJK computing.
    // This is useful for typesetting Latin characters in a CJK  environment. U+FF00 does not correspond to a fullwidth ASCII 20 (space character), since that
    // role is already fulfilled by U+3000 "ideographic space."
    if ((x >= kFullwidthAsciiStart) && (x <= kFullwidthAsciiEnd)) {
        keydown(x - kFullwidthAsciiStart + 0x21);
        return;
    }

    if (x == kIdeographicSpace) {
        keydown(32);
        return;
    }

    x |= UC;
    keydown(x);
}

static inline void keyup_unicode(uint32_t x) {
    // note: UNICODE 0-127 map directly to ASCII 0-127
    if (x <= 127) {
        keyup(x);
        return;
    }

    // note: some UNICODE values map directly to CP437 values found in the extended ASCII set
    auto x2 = unicode_to_cp437(x);
    if (x2) {
        keyup(x2);
        return;
    }

    // note: full width latin characters will be mapped to their normal width equivalents
    // Wikipedia note: Range U+FF01\96FF5E reproduces the characters of ASCII 21 to 7E as fullwidth forms, that is, a fixed width form used in CJK computing.
    // This is useful for typesetting Latin characters in a CJK  environment. U+FF00 does not correspond to a fullwidth ASCII 20 (space character), since that
    // role is already fulfilled by U+3000 "ideographic space."
    if ((x >= kFullwidthAsciiStart) && (x <= kFullwidthAsciiEnd)) {
        keyup(x - kFullwidthAsciiStart + 0x21);
        return;
    }

    if (x == kIdeographicSpace) {
        keyup(32);
        return;
    }

    x |= UC;
    keyup(x);
}

void keyup(uint32_t x) {
    if (!x)
        x = QBK + QBK_CHR0;

    keyheld_remove(x);

    if (asciicode_reading != 2) { // hide numpad presses related to ALT+1+2+3 type entries
        // identify and revert numpad specific key codes to non-numpad codes
        static uint32_t x2;
        static int64_t numpadkey;
        keyboard_try_translate_numpad_keyhit(x, &x2, &numpadkey);

        if (keyboard_keyup_mask_consume(x))
            goto key_handled;

        keyboard_push_keyhit_value(-static_cast<int32_t>(x2), numpadkey);
    } // asciicode_reading!=2

    // static int32_t numlock;
    // numlock = 0;

    if (x <= 255) {
        if (keyboard_scancode_has_variant(x, 0))
            scancodeup(keyboard_scancode_get_scancode(x));
        goto key_handled;
    } // x<=255

    // NUMPAD?
    if ((x >= (VK + QBVK_KP0)) && (x <= (VK + QBVK_KP_ENTER))) {
        // if ((x >= (VK + QBVK_KP0)) && (x <= (VK + QBVK_KP_PERIOD)))
        //     numlock = 1;
        x = (x - (VK + QBVK_KP0) + 256) * 256;
        goto numpadkey;
    }
    if ((x >= (QBK + 0)) && (x <= (QBK + 0 + (QBVK_KP_PERIOD - QBVK_KP0)))) {
        x = (x - (QBK + 0) + 256) * 256;
        goto numpadkey;
    }

    if (x <= 65535) {
        static int32_t r;
    numpadkey:
        r = (x >> 8) + 256;
        if (keyboard_scancode_has_variant(r, 0))
            scancodeup(keyboard_scancode_get_scancode(r));

        if (x == QBKC_INSERT) { // INSERT lock emulation
            update_shift_state();
        }

        goto key_handled;
    } // x<=65536

    { // scope
        uint8_t modifierScancode;
        int32_t modifierFlagsMask;
        if (keyboard_try_get_modifier_data(x, &modifierScancode, &modifierFlagsMask)) {
            (void)modifierFlagsMask;
            scancodeup(modifierScancode);
            update_shift_state();
        }
    }

key_handled:;
}

void keydown(uint32_t x) {
    if (!x)
        x = QBK + QBK_CHR0;

    static int32_t glyph;
    glyph = keydown_glyph;
    keydown_glyph = 0;

    // INSERT lock emulation
    static int32_t insert_held;
    if (x == QBKC_INSERT)
        insert_held = keyheld(QBKC_INSERT);

    // SCROLL lock tracking
    static int32_t scroll_lock_held;
    if (x == (VK + QBVK_SCROLLOCK))
        scroll_lock_held = keyheld(VK + QBVK_SCROLLOCK);

    keyheld_add(x);

    // note: On early keyboards without a Pause key (before the introduction of 101-key keyboards) the Pause function was assigned to Ctrl+NumLock, and the
    // Break function to Ctrl+ScrLock; these key-combinations still work with most programs, even on modern PCs with modern keyboards. CTRL+BREAK handling
    if ((x == (VK + QBVK_BREAK)) || ((x == (VK + QBVK_SCROLLOCK)) && keyboard_is_ctrl_held()) || ((x == (VK + QBVK_F15)) && keyboard_is_ctrl_held())) {
        if (exit_blocked) {
            exit_value |= 2;
            goto key_handled;
        }
        close_program = 1;
        goto key_handled;
    }

#ifdef QB64_WINDOWS
    // note: Alt+F4 is supposed to close the window, but glut windows don't seem to be affected;
    // this addresses the issue:
    if ((x == QBKC_F4) && keyboard_is_alt_held()) {
        if (exit_blocked) {
            exit_value |= 1;
            goto key_handled;
        }
        close_program = 1;
        goto key_handled;
    }
#endif

    // note: On early keyboards without a Pause key (before the introduction of 101-key keyboards) the Pause function was assigned to Ctrl+NumLock, and the
    // Break function to Ctrl+ScrLock; these key-combinations still work with most programs, even on modern PCs with modern keyboards. PAUSE handling
    if ((x == (VK + QBVK_PAUSE)) || ((x == (VK + QBVK_NUMLOCK)) && keyboard_is_ctrl_held())) {
        suspend_program |= 1;
        qbevent = 1;
        goto key_handled;
    } else {
        if (suspend_program & 1) {
            suspend_program ^= 1;
            goto key_handled;
        }
    }

    // ALT+ENTER
    if (keyboard_is_alt_held()) {
        if (x == QBVK_RETURN) {
            if (fullscreen_allowedmode >= 0) { // fullscreen_allowedmode==-1 bypasses alt+enter allowing it to be user-trappable
                libqb_log_trace("ALT+ENTER detected, attempting to change fullscreen mode");

                static int32_t fs_mode, fs_smooth;
                fs_mode = full_screen_set;
                if (fs_mode == -1)
                    fs_mode = full_screen;
                fs_smooth = fullscreen_smooth;

                auto get_fs_combo = [](int32_t mode, int32_t smooth) -> int32_t {
                    if (mode == 0)
                        return 0;
                    if ((mode == 1) && (smooth == 0))
                        return 1;
                    if ((mode == 1) && (smooth == 1))
                        return 2;
                    if ((mode == 2) && (smooth == 0))
                        return 3;
                    return 4;
                };

                auto set_from_fs_combo = [](int32_t combo, int32_t &mode, int32_t &smooth) {
                    switch (combo) {
                    case 0:
                        mode = 0;
                        smooth = 0;
                        break;
                    case 1:
                        mode = 1;
                        smooth = 0;
                        break;
                    case 2:
                        mode = 1;
                        smooth = 1;
                        break;
                    case 3:
                        mode = 2;
                        smooth = 0;
                        break;
                    case 4:
                    default:
                        mode = 2;
                        smooth = 1;
                        break;
                    }
                };

                auto append_mode_combos = [](int32_t mode, int32_t smoothPreference, int32_t *combos, int32_t &comboCount) {
                    // smoothPreference: 1 => smooth first, -1 => off first, 0 => default off first
                    if (smoothPreference == 1) {
                        combos[comboCount++] = (mode == 1 ? 2 : 4);
                        combos[comboCount++] = (mode == 1 ? 1 : 3);
                    } else {
                        combos[comboCount++] = (mode == 1 ? 1 : 3);
                        combos[comboCount++] = (mode == 1 ? 2 : 4);
                    }
                };

                int32_t combos[5];
                int32_t comboCount = 0;
                combos[comboCount++] = 0; // _OFF is always part of the ALT+ENTER cycle unless _ALLOWFULLSCREEN _OFF disables handling entirely

                if (fullscreen_allowedmode > 0) {
                    append_mode_combos(fullscreen_allowedmode, fullscreen_allowedsmooth, combos, comboCount);
                } else {
                    append_mode_combos(1, fullscreen_allowedsmooth, combos, comboCount); // _STRETCH
                    append_mode_combos(2, fullscreen_allowedsmooth, combos, comboCount); // _SQUAREPIXELS
                }

                const int32_t current_combo = get_fs_combo(fs_mode, fs_smooth);
                int32_t current_index = -1;
                for (int32_t i = 0; i < comboCount; i++) {
                    if (combos[i] == current_combo) {
                        current_index = i;
                        break;
                    }
                }
                int32_t next_index = current_index + 1;
                if (next_index >= comboCount)
                    next_index = 0;

                set_from_fs_combo(combos[next_index], fs_mode, fullscreen_smooth);

                // apply
                if (full_screen != fs_mode)
                    full_screen_set = fs_mode;
                force_display_update = 1;
                goto key_handled;
            }
        }
    }

    if (asciicode_reading != 2) { // hide numpad presses related to ALT+1+2+3 type entries
        // identify and revert numpad specific key codes to non-numpad codes
        static uint32_t x2;
        static int64_t numpadkey;
        keyboard_try_translate_numpad_keyhit(x, &x2, &numpadkey);

        // ON KEY trapping
        { // new scope
            static int32_t block_onkey = 0;
            static int32_t f, scancode, extended, flags_mask;
            int32_t i, i2; // must not be static!

            // establish scancode (if any)
            scancode = 0;
            keyboard_get_onkey_scancode_and_flags(x, &scancode, &flags_mask);

            // establish if key is an extended key
            extended = keyboard_is_onkey_extended_key(x);

            if (!block_onkey) {
                // priority #1: user defined keys
                if (scancode) {
                    for (i = 0; i <= 31; i++) {
                        if (onkey[i].key_scancode == scancode) {
                            if (onkey[i].active) {
                                if (onkey[i].id) {
                                    // check keyboard flags
                                    f = onkey[i].key_flags;
                                    // 0 No keyboard flag, 1-3 Either Shift key, 4 Ctrl key, 8 Alt key,32 NumLock key,64 Caps Lock key, 128 Extended keys on a
                                    // 101-key keyboard To specify multiple shift states, add the values together. For example, a value of 12 specifies that the
                                    // user-defined key is used in combination with the Ctrl and Alt keys.
                                    if ((flags_mask & 3) == 0) {
                                        if (f & 3) {
                                            if (keyheld(VK + QBVK_LSHIFT) == 0 && keyheld(VK + QBVK_RSHIFT) == 0)
                                                goto wrong_flags;
                                        } else {
                                            if (keyheld(VK + QBVK_LSHIFT) || keyheld(VK + QBVK_RSHIFT))
                                                goto wrong_flags;
                                        }
                                    }
                                    if ((flags_mask & 4) == 0) {
                                        if (f & 4) {
                                            if (keyheld(VK + QBVK_LCTRL) == 0 && keyheld(VK + QBVK_RCTRL) == 0)
                                                goto wrong_flags;
                                        } else {
                                            if (keyheld(VK + QBVK_LCTRL) || keyheld(VK + QBVK_RCTRL))
                                                goto wrong_flags;
                                        }
                                    }
                                    if ((flags_mask & 8) == 0) {
                                        if (f & 8) {
                                            if (keyheld(VK + QBVK_LALT) == 0 && keyheld(VK + QBVK_RALT) == 0)
                                                goto wrong_flags;
                                        } else {
                                            if (keyheld(VK + QBVK_LALT) || keyheld(VK + QBVK_RALT))
                                                goto wrong_flags;
                                        }
                                    }
                                    if ((flags_mask & 32) == 0) {
                                        if (f & 32) {
                                            if (keyheld(VK + QBVK_NUMLOCK) == 0)
                                                goto wrong_flags;
                                            //*revise
                                        }
                                    }
                                    if ((flags_mask & 64) == 0) {
                                        if (f & 64) {
                                            if (keyheld(VK + QBVK_CAPSLOCK) == 0)
                                                goto wrong_flags;
                                            //*revise
                                        }
                                    }
                                    if ((flags_mask & 128) == 0) {
                                        if (((f & 128) / 128) != extended)
                                            goto wrong_flags;
                                    }
                                    if (onkey[i].active == 1) { //(1)ON
                                        onkey[i].state++;
                                    } else { //(2)STOP
                                        onkey[i].state = 1;
                                    }

                                    qbevent = 1;

                                    // mask trigger key
                                    keyboard_keyup_mask_add(x);

                                    goto key_handled;

                                } // id
                            } // active
                        } // scancode==
                    wrong_flags:;
                    } // i
                } // scancode

                // priority #2: fixed index F1-F12, arrows
                for (i = 0; i <= 31; i++) {
                    if (onkey[i].active) {
                        if (onkey[i].id) {
                            if ((x2 == onkey[i].keycode) || x == onkey[i].keycode_alternate) {
                                if (onkey[i].active == 1) { //(1)ON
                                    onkey[i].state++;
                                } else { //(2)STOP
                                    onkey[i].state = 1;
                                }
                                qbevent = 1;

                                // mask trigger key
                                keyboard_keyup_mask_add(x);

                                goto key_handled;
                            } // keycode
                        } // id
                    } // active
                } // i

            } // block_onkey

            // priority #3: string insertion
            for (i = 0; i <= 31; i++) {
                if (onkey[i].text) {
                    if (onkey[i].text->len) {
                        if ((x2 == onkey[i].keycode) || x == onkey[i].keycode_alternate) {
                            // mask trigger key
                            keyboard_keyup_mask_add(x);

                            for (i2 = 0; i2 < onkey[i].text->len; i2++) {
                                block_onkey = 1;
                                keydown(onkey[i].text->chr[i2]);
                                keyup(onkey[i].text->chr[i2]);
                                block_onkey = 0;
                            } // i2
                            goto key_handled;
                        } // keycode
                    }
                } // text
            } // i
        } // descope

        /*
            //keyhit cyclic buffer
            int64_t keyhit[8192];
            //    keyhit specific internal flags: (stored in high 32-bits)
            //    &4294967296->numpad was used
            int32_t keyhit_nextfree=0;
            int32_t keyhit_next=0;
            //note: if full, the oldest message is discarded to make way for the new message
        */
        keyboard_push_keyhit_value(static_cast<int32_t>(x2), numpadkey);
    } // asciicode_reading!=2

    static int32_t shift, alt, ctrl, numlock;
    numlock = 0;

    if (x == QBK + QBK_CHR0)
        x = 0;

    if (x <= 255) {
        static int32_t b1, b2, z, o;
        b1 = x;
        if ((b2 = keyboard_scancode_get_scancode(x))) { // table entry exists
            scancodedown(b2);

            // check for relevant table modifiers
            keyboard_get_modifier_triplet(&shift, &ctrl, &alt);
            o = 0;
            if (shift)
                o = 1;
            if (ctrl)
                o = 2;
            if (alt)
                o = 3;
            if (glyph) {
                if (keyboard_is_altgr_combo())
                    o = 0; // assume alt-gr combo-key
            }
            z = keyboard_scancode_get_variant(x, o);
            if (!z)
                goto key_handled; // not possible
            if (z & kAsciiMask) {
                b1 = 0;
                b2 = z >> 8;
            } else {
                b1 = z;
            }
        } // b2
        keyboard_push_bios_keystroke(b1, b2);
        goto key_handled;
    } // x<=255

    // NUMPAD?
    if ((x >= (VK + QBVK_KP0)) && (x <= (VK + QBVK_KP_ENTER))) {
        if ((x >= (VK + QBVK_KP0)) && (x <= (VK + QBVK_KP_PERIOD)))
            numlock = 1;
        x = (x - (VK + QBVK_KP0) + 256) * 256;
        goto numpadkey;
    }
    if ((x >= (QBK + 0)) && (x <= (QBK + 0 + (QBVK_KP_PERIOD - QBVK_KP0)))) {
        x = (x - (QBK + 0) + 256) * 256;
        goto numpadkey;
    }

    if (x <= 65535) {
        static int32_t b1, b2, z, o, r;
    numpadkey:
        b1 = 0;
        b2 = x >> 8;
        r = (x >> 8) + 256;
        if (keyboard_scancode_has_variant(r, 0)) {
            scancodedown(keyboard_scancode_get_scancode(r));
            // check relevant modifiers
            keyboard_get_modifier_triplet(&shift, &ctrl, &alt);

            if (x == QBKC_INSERT) {     // INSERT lock emulation
                if (insert_held == 0) { // nullify effects of key repeats
                    if ((alt == 0) && (shift == 0) && (ctrl == 0)) {
                        // toggle insert mode
                        if (keyheld(QBK + QBK_INSERT_MODE)) {
                            keyheld_remove(QBK + QBK_INSERT_MODE);
                        } else {
                            keyheld_add(QBK + QBK_INSERT_MODE);
                        }
                        update_shift_state();
                    }
                }
            }

            o = 0;
            if (shift)
                o = 1;
            if (numlock)
                o = 4;
            if (numlock && shift)
                o = 7;
            if (ctrl)
                o = 2;
            if (alt)
                o = 3;
            z = keyboard_scancode_get_variant(r, o);
            if (!z)
                goto key_handled; // invalid combination
            if (z & kAsciiMask) {
                b1 = 0;
                b2 = z >> 8;
            } else {
                b1 = z;
                b2 = keyboard_scancode_get_scancode(r);
            }
        } // z
        keyboard_push_bios_keystroke(b1, b2);
        goto key_handled;
    } // x<=65536

    { // scope
        uint8_t modifierScancode;
        int32_t modifierFlagsMask;
        if (keyboard_try_get_modifier_data(x, &modifierScancode, &modifierFlagsMask)) {
            scancodedown(modifierScancode);

            if (x == (VK + QBVK_SCROLLOCK)) {
                if (scroll_lock_held == 0) { // nullify effects of key repeats
                    ctrl = keyboard_is_ctrl_held();
                    if (ctrl == 0) {
                        // toggle insert mode
                        if (keyheld(QBK + QBK_SCROLL_LOCK_MODE)) {
                            keyheld_remove(QBK + QBK_SCROLL_LOCK_MODE);
                        } else {
                            keyheld_add(QBK + QBK_SCROLL_LOCK_MODE);
                        }
                    }
                }
            }

            update_shift_state();
        }
    }

key_handled:
    sleep_break = 1;
}

void update_shift_state() {
    int32_t x;
    /*
        0:417h                   Shift Status
        7 6 5 4 3 2 1 0
        x . . . . . . .      Insert locked
        . x . . . . . .      Caps Lock locked
        . . x . . . . .      Num Lock locked
        . . . x . . . .      Scroll Lock locked
        . . . . x . . .      Alt key is pressed
        . . . . . x . .      Ctrl key is pressed
        . . . . . . x .      Left Shift key is pressed
        . . . . . . . x      Right Shift key is pressed
    */
    x = 0;
    if (keyheld(VK + QBVK_RSHIFT))
        x |= 1;
    if (keyheld(VK + QBVK_LSHIFT))
        x |= 2;
    if (keyheld(VK + QBVK_LCTRL) || keyheld(VK + QBVK_RCTRL))
        x |= 4;
    if (keyheld(VK + QBVK_LALT) || keyheld(VK + QBVK_RALT))
        x |= 8;
    if (keyheld(QBK + QBK_SCROLL_LOCK_MODE))
        x |= 16;
    if (keyheld(VK + QBVK_NUMLOCK))
        x |= 32;
    if (keyheld(VK + QBVK_CAPSLOCK))
        x |= 64;
    // note: insert state is emulated (off by default)
    if (keyheld(QBK + QBK_INSERT_MODE))
        x |= 128;
    cmem[kBiosShiftStatusOffset] = x;
    /*
        0:418h                   Extended Shift Status
        (interpret the word 'pressed' as "being held down")
        7 6 5 4 3 2 1 0
        x . . . . . . .      Ins key is pressed
        . x . . . . . .      Caps Lock key is pressed (detection not possible, return 0)
        . . x . . . . .      Num Lock key is pressed (detection not possible, return 0)
        . . . x . . . .      Scroll Lock key is pressed
        . . . . x . . .      Pause key locked
        . . . . . x . .      SysReq key is pressed
        . . . . . . x .      Left Alt key is pressed
        . . . . . . . x      Left Ctrl key is pressed
    */
    x = 0;
    if (keyheld(VK + QBVK_LCTRL))
        x |= 1;
    if (keyheld(VK + QBVK_LALT))
        x |= 2;
    if (keyheld(VK + QBVK_SYSREQ))
        x |= 4;
    if (keyheld(VK + QBVK_PAUSE))
        x |= 8;
    if (keyheld(VK + QBVK_SCROLLOCK))
        x |= 16;
    // if (keyheld(VK+QBVK_NUMLOCK)) x|=32;
    // if (keyheld(VK+QBVK_CAPSLOCK)) x|=64;
    if (keyheld(QBKC_INSERT))
        x |= 128;
    cmem[kBiosExtendedShiftStatusOffset] = x;
    /*
        0:496h                   Keyboard Status and Type Flags
        This byte holds keyboard status information.
        Keyboard Status Information
        7 6 5 4 3 2 1 0
        x . . . . . . .       Read ID in progress (always 0)
        . x . . . . . .       Last character was first ID character (always 0)
        . . x . . . . .       Force Num Lock if read ID and KBX (always 0)
        . . . x . . . .       101/102-key keyboard installed (always 1)
        . . . . x . . .       Right Alt key is pressed
        . . . . . x . .       Right Ctrl key is pressed
        . . . . . . x .       Last code was E0 Hidden Code (always 0)
        . . . . . . . x       last code was E1 Hidden Code (always 0)
    */
    x = 0;
    if (keyheld(VK + QBVK_RCTRL))
        x |= 1;
    if (keyheld(VK + QBVK_RALT))
        x |= 2;
    x |= 16;
    cmem[kBiosKeyboardStatusFlagsOffset] = x;
}

