static inline void keyboard_push_keyhit_value(int32_t value, int64_t keyhitFlag) {
    int32_t nextKeyhit = (keyhit_nextfree + 1) & kKeyhitRingMask;
    if (nextKeyhit == keyhit_next) { // remove oldest message when cyclic buffer is full
        keyhit_next = (keyhit_next + 1) & kKeyhitRingMask;
    }
    keyhit[keyhit_nextfree] = static_cast<int64_t>(static_cast<uint32_t>(value)) | keyhitFlag;
    keyhit_nextfree = nextKeyhit;
}

static inline void keyboard_push_bios_keystroke(int32_t b1, int32_t b2) {
    int32_t head = cmem[kBiosKeyboardBufferHeadOffset];
    int32_t tail = cmem[kBiosKeyboardBufferTailOffset];
    int32_t nextTail = tail + 2;
    if (nextTail == kBiosKeyboardBufferEndExclusive)
        nextTail = kBiosKeyboardBufferStart;
    if (head != nextTail) { // fits in buffer
        cmem[kBiosKeyboardDataOffset + tail] = b1;
        cmem[kBiosKeyboardDataOffset + tail + 1] = b2;  // (scancode)
        cmem[kBiosKeyboardBufferTailOffset] = nextTail; // fix tail location
    }
}

static inline bool keyboard_try_translate_numpad_keyhit(uint32_t key, uint32_t *normalizedKey, int64_t *keyhitFlag) {
    *normalizedKey = key;
    *keyhitFlag = 0;

    if ((key >= (VK + QBVK_KP0)) && (key <= (VK + QBVK_KP_ENTER))) {
        *keyhitFlag = kKeyhitFlagNumpad;

        if ((key >= (VK + QBVK_KP0)) && (key <= (VK + QBVK_KP9))) {
            *normalizedKey = key - (VK + QBVK_KP0) + 48;
            return true;
        }

        switch (key) {
        case VK + QBVK_KP_PERIOD:
            *normalizedKey = 46;
            return true;
        case VK + QBVK_KP_DIVIDE:
            *normalizedKey = 47;
            return true;
        case VK + QBVK_KP_MULTIPLY:
            *normalizedKey = 42;
            return true;
        case VK + QBVK_KP_MINUS:
            *normalizedKey = 45;
            return true;
        case VK + QBVK_KP_PLUS:
            *normalizedKey = 43;
            return true;
        case VK + QBVK_KP_ENTER:
            *normalizedKey = 13;
            return true;
        }
    }

    if ((key >= (QBK + 0)) && (key <= (QBK + 10))) {
        *keyhitFlag = kKeyhitFlagNumpad;
        const uint32_t offset = key - QBK;
        static constexpr uint16_t kQbkNumpadLookup[] = {82 << 8, 79 << 8, 80 << 8, 81 << 8, 75 << 8, 76 << 8, 77 << 8, 71 << 8, 72 << 8, 73 << 8, 83 << 8};

        *normalizedKey = kQbkNumpadLookup[offset];
        return true;
    }

    return false;
}

static constexpr inline bool keyboard_try_get_modifier_data(uint32_t key, uint8_t *scancode, int32_t *flagsMask) {
    switch (key) {
    case VK + QBVK_LSHIFT:
        *scancode = 42;
        *flagsMask = 3;
        return true;
    case VK + QBVK_RSHIFT:
        *scancode = 54;
        *flagsMask = 3;
        return true;
    case VK + QBVK_LALT:
    case VK + QBVK_RALT:
        *scancode = 56;
        *flagsMask = 8;
        return true;
    case VK + QBVK_LCTRL:
    case VK + QBVK_RCTRL:
        *scancode = 29;
        *flagsMask = 4;
        return true;
    case VK + QBVK_NUMLOCK:
        *scancode = 69;
        *flagsMask = 32;
        return true;
    case VK + QBVK_CAPSLOCK:
        *scancode = 58;
        *flagsMask = 64;
        return true;
    case VK + QBVK_SCROLLOCK:
        *scancode = 70;
        *flagsMask = 0;
        return true;
    default:
        return false;
    }
}

static inline void keyboard_get_onkey_scancode_and_flags(uint32_t key, int32_t *scancode, int32_t *flagsMask) {
    if (key <= 255) {
        *scancode = keyboard_scancode_get_scancode(key);
    } else {
        uint32_t x3 = key;

        if ((x3 >= (VK + QBVK_KP0)) && (x3 <= (VK + QBVK_KP_ENTER))) {
            x3 = (x3 - (VK + QBVK_KP0) + 256) * 256;
        } else if ((x3 >= (QBK + 0)) && (x3 <= (QBK + 0 + (QBVK_KP_PERIOD - QBVK_KP0)))) {
            x3 = (x3 - (QBK + 0) + 256) * 256;
        }

        if (x3 <= 65535) {
            int32_t r = (x3 >> 8) + 256;
            if (keyboard_scancode_has_variant(r, 0))
                *scancode = keyboard_scancode_get_scancode(r);
        }
    }

    uint8_t modifierScancode;
    if (keyboard_try_get_modifier_data(key, &modifierScancode, flagsMask))
        *scancode = modifierScancode;
}

